TEST( Trie_Tests, partial_match ) {
    auto str     = std::u32string( U"&not" );
    auto trie    = Trie<uint32_t>();
    auto test    = std::u32string( U"&notit" );
    auto tracker = TrieTracker<uint32_t>();

    trie.add( str.cbegin(), str.cend() );

    for( auto c : test ) {
        trie.match( tracker, c );
    }

    ASSERT_TRUE( tracker.matched() );
    ASSERT_TRUE( tracker.complete() );
    ASSERT_FALSE( tracker.partial() );
    ASSERT_FALSE( tracker.matching() );
    ASSERT_EQ( tracker.lastCompleteMatchSize(), 4 );
}