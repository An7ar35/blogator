/**
 * Constructor (default)
 */
NamedCharRef::NamedCharRef() :
    codepoint1( 0 ),
    codepoint2( 0 )
{}

/**
 * Constructor
 * @param name Name of character reference
 * @param codepoint Code-point
 */
NamedCharRef::NamedCharRef( std::u32string name, uint32_t codepoint ) :
    name( std::move( name ) ),
    codepoint1( codepoint ),
    codepoint2( 0 )
{}

/**
 * Constructor
 * @param name Name of character reference
 * @param codepoint1 Code-point
 * @param codepoint2 Secondary code-point
 */
NamedCharRef::NamedCharRef( std::u32string name, uint32_t codepoint1, uint32_t codepoint2 ) :
    name( std::move( name ) ),
    codepoint1( codepoint1 ),
    codepoint2( codepoint2 )
{}

/**
 * Equal operator
 * @param rhs NamedCharRef to compare to
 * @return Equal state
 */
bool NamedCharRef::operator ==( const NamedCharRef &rhs ) const {
    return ( name == rhs.name );
}

/**
 * Not equal operator
 * @param rhs NamedCharRef to compare to
 * @return Not equal state
 */
bool NamedCharRef::operator !=( const NamedCharRef &rhs ) const {
    return ( name != rhs.name );
}

/**
 * Smaller-than operator
 * @param rhs NamedCharRef to compare to
 * @return Smaller-than state
 */
bool NamedCharRef::operator <( const NamedCharRef &rhs ) const {
    return ( name < rhs.name );
}

/**
 * Greater-than operator
 * @param rhs NamedCharRef to compare to
 * @return Greater-than state
 */
bool NamedCharRef::operator >( const NamedCharRef &rhs ) const {
    return ( name > rhs.name );
}

/**
 * Smaller-equal operator
 * @param rhs NamedCharRef to compare to
 * @return Smaller-equal state
 */
bool NamedCharRef::operator <=( const NamedCharRef &rhs ) const {
    return ( name <= rhs.name );
}

/**
 * Greater-equal operator
 * @param rhs NamedCharRef to compare to
 * @return Greater-equal state
 */
bool NamedCharRef::operator >=( const NamedCharRef &rhs ) const {
    return ( name >= rhs.name );
}

