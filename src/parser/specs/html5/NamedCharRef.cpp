/**
 * Gets the matching named character reference
 * @param name Name to look for
 * @return { found flag, Named character reference }
 */
std::pair<bool, const NamedCharRef &> getNamedCharRef( const std::u32string &name ) {
    if( named_char_references.empty() )
        loadNamedCharReferences( named_char_references, not_found );

    auto it = std::lower_bound( named_char_references.cbegin(),
                                named_char_references.cend(),
                                NamedCharRef( name, 0, 0 ) );

    if( it == named_char_references.cend() ) {
        return { false, *not_found };
    } else {
        return { true, *it };
    }
}