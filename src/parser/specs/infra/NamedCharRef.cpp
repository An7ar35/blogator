#include "NamedCharRef.h"

#include "../../../sys/terminate.h"

using namespace blogator::parser::specs::infra;

NamedCharRef                                     NamedCharRef::_not_found = loadNotFound();
std::unordered_map<std::u32string, NamedCharRef> NamedCharRef::_ncr_map   = loadMap();
blogator::Trie<char32_t>                         NamedCharRef::_ncr_trie  = loadTrie();

/**
 * Constructor (default)
 */
NamedCharRef::NamedCharRef() :
    codepoint1( 0 ),
    codepoint2( 0 )
{}

/**
 * Constructor
 * @param name Name of character reference
 * @param codepoint Code-point
 */
NamedCharRef::NamedCharRef( std::u32string name, char32_t codepoint ) :
    name( std::move( name ) ),
    codepoint1( codepoint ),
    codepoint2( 0 )
{}

/**
 * Constructor
 * @param name Name of character reference
 * @param codepoint1 Code-point
 * @param codepoint2 Secondary code-point
 */
NamedCharRef::NamedCharRef( std::u32string name, char32_t codepoint1, char32_t codepoint2 ) :
    name( std::move( name ) ),
    codepoint1( codepoint1 ),
    codepoint2( codepoint2 )
{}

/**
 * Equal operator
 * @param rhs NamedCharRef to compare to
 * @return Equal state
 */
bool NamedCharRef::operator ==( const NamedCharRef &rhs ) const {
    return ( name == rhs.name );
}

/**
 * Not equal operator
 * @param rhs NamedCharRef to compare to
 * @return Not equal state
 */
bool NamedCharRef::operator !=( const NamedCharRef &rhs ) const {
    return ( name != rhs.name );
}

/**
 * Smaller-than operator
 * @param rhs NamedCharRef to compare to
 * @return Smaller-than state
 */
bool NamedCharRef::operator <( const NamedCharRef &rhs ) const {
    return ( name < rhs.name );
}

/**
 * Greater-than operator
 * @param rhs NamedCharRef to compare to
 * @return Greater-than state
 */
bool NamedCharRef::operator >( const NamedCharRef &rhs ) const {
    return ( name > rhs.name );
}

/**
 * Smaller-equal operator
 * @param rhs NamedCharRef to compare to
 * @return Smaller-equal state
 */
bool NamedCharRef::operator <=( const NamedCharRef &rhs ) const {
    return ( name <= rhs.name );
}

/**
 * Greater-equal operator
 * @param rhs NamedCharRef to compare to
 * @return Greater-equal state
 */
bool NamedCharRef::operator >=( const NamedCharRef &rhs ) const {
    return ( name >= rhs.name );
}

/**
 * Gets the matching named character reference
 * @param name Name to look for
 * @return { found flag, Named character reference }
 */
std::pair<bool, const NamedCharRef &> NamedCharRef::fetch( const std::u32string &name ) {
    if( NamedCharRef::_ncr_map.contains( name ) ) {
        return { true, NamedCharRef::_ncr_map.at( name ) };
    } else {
        return { false, NamedCharRef::_not_found };
    }
}

/**
 * Do iterative matching for named character references
 * @param tracker Matching tracker state object
 * @param next_element Next element to match
 * @return Match state
 */
bool NamedCharRef::match( blogator::TrieTracker<char32_t> &tracker, char32_t next_element ) {
    return NamedCharRef::_ncr_trie.match( tracker, next_element );
}

/**
 * [PRIVATE] Initialises static 'not found' NamedCharRef object
 * @return NamedCharRef
 */
NamedCharRef NamedCharRef::loadNotFound() noexcept {
    try {
        return {};

    } catch( ... ) {
        TERMINATE( "[parser::specs::infra::NamedCharRef::loadNotFound()] Failed to initialise." );
    }
}

